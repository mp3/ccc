// Test bitwise operators
int main() {
    int a = 15;   // 0x0F = 00001111
    int b = 240;  // 0xF0 = 11110000
    
    int c = a & b;  // AND: 00001111 & 11110000 = 00000000 = 0
    int d = a | b;  // OR:  00001111 | 11110000 = 11111111 = 255
    int e = a ^ b;  // XOR: 00001111 ^ 11110000 = 11111111 = 255
    int f = ~a;     // NOT: ~00001111 = 11111111111111111111111111110000 = -16
    int g = a << 2; // LSH: 00001111 << 2 = 00111100 = 60
    int h = b >> 2; // RSH: 11110000 >> 2 = 00111100 = 60
    
    return c + d + e + f + g + h;  // 0 + 255 + 255 + (-16) + 60 + 60 = 614
    // Exit code will be 614 & 0xFF = 102
}
